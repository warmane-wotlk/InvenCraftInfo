local colorHeader = { "|cffff7f3f", "|cffffff00", "|cff3fbf3f", "|cff7f7f7f" }
local color, colorText = {}

function InvenCraftInfo:GetSpellColor(id)
	if id and data[id] then
		color[1], color[2], color[3], color[4] = (","):split(data[id])
		colorText = ""
		for i = 1, 4 do
			if color[i] and color[i] ~= "0" then
				colorText = colorText..(colorText ~= "" and " " or "")..colorHeader[i]..color[i]
			end
		end
		return colorText..(colorText ~= "" and "|r" or "")
	else
		return ""
	end
end

function InvenCraftInfo:GetSpellColorData(id)
	return id and data[id]
