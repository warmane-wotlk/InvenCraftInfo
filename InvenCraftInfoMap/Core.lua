function InvenCraftInfoMap:CheckZone(zone)
	return zone and mapInfo[zone]
end

local function GetMapTextureSize(index, num, size)
	if index < num then
		pixelSize = 256
		fileSize = 256
	else
		pixelSize = mod(size, 256)
		if pixelSize == 0 then
			pixelSize = 256
		end
		fileSize = 16
		while fileSize < pixelSize do
			fileSize = fileSize * 2
		end
	end
	return pixelSize, fileSize, 256
end

function InvenCraftInfoMap:ShowMap(npcID)
	if InvenCraftInfo:LoadData() then return end
	if type(npcID) == "string" then
		npcID = strtrim(npcID:match("(.+)%(") or npcID)
		if self:CheckZone(npcID) then
			self:SetMap(npcID)
		elseif instanceLocationDB[npcID] and self:CheckZone(instanceLocationDB[npcID][1]) then
			self:SetMap(instanceLocationDB[npcID][1])
			self:SetInstancePosition(npcID, unpack(instanceLocationDB[npcID]))
		elseif self.frame then
			self.frame:Hide()
		end
	else
		npcName, zone1, zone2, px, py = InvenCraftInfo:GetNPCData(npcID)
		if npcName and npcID > 0 then
			if self:CheckZone(zone1) then
				if zone1 == "달라란" and zone2 == "마법의 뒤안길" then
					zone1 = zone1.." "..zone2
				end
				self:SetMap(zone1)
				if type(px) == "number" and type(py) == "number" then
					self:SetPOIposition(npcName, zone1, px, py)
				elseif npcPositionDB[npcID] then
					px, py, px2, py2 = strsplit(",", npcPositionDB[npcID])
					if px and py and px2 and py2 then
						self:SetPOIrange(npcName, zone1, px, py, px2, py2)
					elseif px and py then
						self:SetPOIposition(npcName, zone1, px, py)
					end
				end
			elseif zone1 and zone2 and instanceLocationDB[zone1.." "..zone2] then
				self:SetMap(instanceLocationDB[zone1.." "..zone2][1])
				self:SetInstancePosition(npcName, zone1.." "..zone2, instanceLocationDB[zone1.." "..zone2][2], instanceLocationDB[zone1.." "..zone2][3])
			elseif zone1 and instanceLocationDB[zone1] then
				self:SetMap(instanceLocationDB[zone1][1])
				self:SetInstancePosition(npcName, zone1, instanceLocationDB[zone1][2], instanceLocationDB[zone1][3])
			end
		elseif self.frame then
			self.frame:Hide()
		end
	end
end

function InvenCraftInfoMap:SetMap(zone)
	if self.frame then self.frame:Hide() end
	if not self:CheckZone(zone) then return end
	if not self.frame then self:CreateFrame() end
	if self.currentZone ~= zone then
		self.currentZone = zone
		if dungeonLevel[zone] then
			tilePath = format("Interface\\WorldMap\\%s\\%s%d_", mapInfo[zone], mapInfo[zone], dungeonLevel[zone])
		else
			tilePath = format("Interface\\WorldMap\\%s\\%s", mapInfo[zone], mapInfo[zone])
		end
		for i = 1, 12 do
			self.frame.map.tiles[i]:SetTexture(tilePath..i)
		end
		textureCount = 0
		if mapOverlays[mapInfo[zone]] then
			for texture, textureID in pairs(mapOverlays[mapInfo[zone]]) do
				texWidth, texHeight, texOffsetX, texOffsetY = mod(textureID, 2^10), mod(floor(textureID / 2 ^ 10), 2 ^ 10), mod(floor(textureID / 2 ^ 20), 2 ^ 10), floor(textureID / 2 ^ 30)
				ntexWide = ceil(texWidth / 256)
				ntexTall = ceil(texHeight / 256)
				neededTextures = textureCount + (ntexWide * ntexTall)
				for i = #self.frame.map.overlays + 1, neededTextures do
					self.frame.map.overlays[i] = self.frame.map:CreateTexture("InvenCraftInfoMapFrameOverlay"..i, "ARTWORK")
				end
				for i = 1, ntexTall do
					texturePixelHeight, textureFileHeight = GetMapTextureSize(i, ntexTall, texHeight)
					for j = 1, ntexWide do
						texturePixelWidth, textureFileWidth = GetMapTextureSize(j, ntexWide, texWidth)
						textureCount = textureCount + 1
						self.frame.map.overlays[textureCount]:SetWidth(texturePixelWidth)
						self.frame.map.overlays[textureCount]:SetHeight(texturePixelHeight)
						self.frame.map.overlays[textureCount]:SetTexCoord(0, texturePixelWidth / textureFileWidth, 0, texturePixelHeight / textureFileHeight)
						self.frame.map.overlays[textureCount]:ClearAllPoints()
						self.frame.map.overlays[textureCount]:SetPoint("TOPLEFT", self.frame.map, "TOPLEFT", (texOffsetX + (256 * (j - 1))), -(texOffsetY + (256 * (i - 1))))
						self.frame.map.overlays[textureCount]:SetTexture(format("Interface\\WorldMap\\%s\\%s%d", mapInfo[zone], texture, ((i - 1) * ntexWide) + j))
						self.frame.map.overlays[textureCount]:Show()
					end
				end
			end
		end
		for i = textureCount + 1, #self.frame.map.overlays do
			self.frame.map.overlays[i]:Hide()
		end
	end
	self.frame.poi:Hide()
	self.frame.instance:Hide()
	for i = 1, #self.frame.range do
		self.frame.range[i]:Hide()
	end
	self.frame:Show()
	self.frame.title:SetText("Inven Craft Info Map")
end

