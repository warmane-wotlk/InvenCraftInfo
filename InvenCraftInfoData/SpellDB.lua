function InvenCraftInfo:GetRecipeID(item)
	item = self:GetLinkID(item, "item")
	if item then
		for p, v in pairs(data) do
			if v[item] then
				if GetSpellInfo(v[item]) then
					return v[item], p
				else
					break
				end
			end
		end
		for p, v in pairs(forceMatchData) do
			if v[item] then
				if GetSpellInfo(v[item]) then
					return v[item], p
				else
					break
				end
			end
		end
	end
	return nil, nil
end

function InvenCraftInfo:GetSpellID(id)
	for p, v in pairs(data) do
		if v[id] and GetSpellInfo(v[id]) then
			return v[id]
		end
	end
	for p, v in pairs(forceMatchData) do
		if v[id] and GetSpellInfo(v[id]) then
			return v[id]
		end
	end
end

function InvenCraftInfo:IsMakeableItem(skill, item)
	if skill and data[skill] then
		item = self:GetLinkID(item, "item")
		if item then
			return data[skill][item]
		end
	end
	return nil
end

function InvenCraftInfo:GetSkillTable(skill)
	return data[skill or "*"]
