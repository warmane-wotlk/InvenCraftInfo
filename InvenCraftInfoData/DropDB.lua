					f, s = data[id][i]:match("F(%d+)_(%d+)")
					if f and s then
						v = (v and (v.."\n") or "")..format("%s: %s 평판 필요", factionAll[tonumber(f)] or faction[tonumber(f)], fstatus[tonumber(s)])
					else
						v = (v and (v.."\n") or "")..data[id][i]
					end
				elseif type(data[id][i]) == "number" and self:GetNPCInfoAll(data[id][i]) then
					v = (v and (v.."\n") or "")..self:GetNPCInfoAll(data[id][i])
				end
			end
			return v
		end
	end
	return nil
end

local function getSource(db)
	if type(db) == "string" then
		if db:find("이벤트") then
			return "E"
		elseif db:find("퀘스트") or db:find("^%[(%d+)%] ") or db:find("^%[(%d+)%+%] ") then
			return "Q"
		elseif db:find("몬스터") then
			return "D"
		elseif db:find("F(%d+)_(%d+)") then
			return "F"
		elseif db:find("명예 점수") or db:find("투기장 점수") then
			return "H"
		end
	elseif db < 0 then
		return "A"
	elseif db == 0 then
		return "W"
	elseif type(db) == "number" then
		if InvenCraftInfo:GetNPCLocation(db) then
			return "V"
		else
			return "D"
		end
	end
	return "T"
end

function InvenCraftInfo:GetSpellSource(id)
	if id and data[id] then
		if type(data[id]) == "table" then
			v = {}
			for i = 1, #data[id] do
				f = getSource(data[id][i])
				if f then
					if f == "F" then
						return f
					else
						v[f] = true
					end
				end
			end
			f = nil
			for p in pairs(v) do
				f = (f and (f..",") or "")..p
			end
			return f or "T"
		else
			return getSource(data[id])
		end
	end
	return "T"
end

function InvenCraftInfo:GetSpellSourceString(id)
	if id and data[id] then
		if type(data[id]) == "table" then
			for i = #data[id], 1, -1 do
				if type(data[id][i]) == "string" then
					return data[id][i]
				end
			end
		elseif type(data[id]) == "string" then
			return data[id]
		end
	end
	return nil
end

