end

function InvenCraftInfo:GetNPCData(id)
	if id and data[id] and type(data[id]) == "table" then
		return unpack(data[id])
	end
end

function InvenCraftInfo:GetNPCInfo(id)
	if data[id] then
		if type(data[id]) == "table" then
			if (data[id][6] == 0 and f == "H") or (data[id][6] == 1 and f == "A") then
				return nil
			end
			v = nil
			if data[id][2] then
				if isRaid[data[id][2]] then
					if data[id][2]:find("(.+)%((.+)%)") then
						v = data[id][2]:gsub("(.+)%((.+)%)", "|cffff7db4%1|r(%2)")
					else
						v = "|cffff7db4"..data[id][2]
						if data[id][3] then
							v = v.."("..data[id][3]..")|r"
						else
							v = v.."|r"
						end
					end
				else
					v = "|cffffffa4"..data[id][2]
					if data[id][3] then
						v = v.."("..data[id][3]..")|r"
					else
						v = v.."|r"
					end
				end
			end
			v = (v and (v.." ") or " ")..data[id][1]
			if data[id][4] and data[id][4] ~= 0 then
				v = v..(" %.1f, %.1f"):format(data[id][4], data[id][5])
			end
			return v
		else
			return data[id]
		end
	end
	return nil
end

