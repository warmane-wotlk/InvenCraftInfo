function InvenCraftInfo:GetDrop(id)
	if id and data[id] then
		if type(data[id]) == "string" then
			f, s = data[id]:match("F(%d+)_(%d+)")
			if f and s then
				return format("%s: %s 평판 필요", faction[tonumber(f)], fstatus[tonumber(s)])
			end
			return data[id]
		elseif type(data[id]) == "number" then
			return self:GetNPCInfo(data[id])
		elseif type(data[id]) == "table" then
			v = nil
			for i = 1, #data[id] do
				if type(data[id][i]) == "string" then
					f, s = data[id][i]:match("F(%d+)_(%d+)")
					if f and s then
						v = (v and (v.."\n") or "")..format("%s: %s 평판 필요", faction[tonumber(f)], fstatus[tonumber(s)])
					else
						v = (v and (v.."\n") or "")..data[id][i]
					end
				elseif type(data[id][i]) == "number" and self:ExistsNPC(data[id][i]) then
					v = (v and (v.."\n") or "")..self:GetNPCInfo(data[id][i])
				end
			end
			return v
		end
	end
	return nil
end

function InvenCraftInfo:GetDropNPCID(id)
	if id and data[id] then
		if type(data[id]) == "number" and data[id] > 0 then
			return data[id], 1
		elseif type(data[id]) == "table" then
			v = nil
			v1 = 0
			for i = 1, #data[id] do
				if type(data[id][i]) == "string" then
					v = v and (v..",0") or "0"
					v1 = v1 + 1
				elseif type(data[id][i]) == "number" and self:ExistsNPC(data[id][i]) then
					v = (v and (v..",") or "")..data[id][i]
					v1 = v1 + 1
				end
			end
			return v, v1

		end
	end
	return nil, 0
end

