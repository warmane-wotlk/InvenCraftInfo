		self.frame.range[i] = CreateFrame("Button", nil, self.frame)
		self.frame.range[i]:SetAlpha(0.4)
		self.frame.range[i]:SetNormalTexture("Interface\\AddOns\\InvenCraftInfoMap\\Circle.tga")
		self.frame.range[i].texture = self.frame.range[i]:GetNormalTexture()
		self.frame.range[i]:SetScript("OnEnter", self.frame.poi:GetScript("OnEnter"))
		self.frame.range[i]:SetScript("OnLeave", self.frame.poi:GetScript("OnLeave"))
		if i == 1 then
			self.frame.range[i]:SetScript("OnUpdate", function(self, eltimer)
				self.eltimer = (self.eltimer or 0) + eltimer
				if self.eltimer >= 0.5 then
					self.eltimer = 0
					self.color = not self.color
					if self.color then
						self.texture:SetVertexColor(1, 0, 0)
					else
						self.texture:SetVertexColor(0, 0, 1)
					end
				end
			end)
		else
			self.frame.range[i]:SetScript("OnUpdate", self.frame.range[1]:GetScript("OnUpdate"))
		end
	end
	self.frame.range[i].color = true
	self.frame.range[i].eltimer = 0
	self.frame.range[i].texture:SetVertexColor(1, 0, 0)
	self.frame.range[i]:SetFrameLevel(self.frame:GetFrameLevel() + 2)
	self.frame.range[i]:ClearAllPoints()
	self.frame.range[i]:SetPoint("TOPLEFT", self.frame.map, "TOPLEFT", self.frame.map.width * (x1 / 100) * self.frame.map.scale, -self.frame.map.height * (y1 / 100) * self.frame.map.scale)
